
public class CPUWaterblock {
	String socket; 
	String metal;
	String name;

	public CPUWaterblock(String socket, String metal, String name) {
		this.socket = socket; 
		this.metal = metal; 
		this.name = name; 
	}
	
	public String getName() {
		return name;
	}
	public String getSocket() {
		return socket;
	}
	public String getMetal() {
		return metal; 
	}
	public String toString() {
		
		return name;
	}



}
	