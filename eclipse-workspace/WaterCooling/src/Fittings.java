
public class Fittings {

String name; 
String diameter;

	
	public Fittings(String name, String diameter) {
		this.name = name; 
		this.diameter = diameter; 
	}
	public String getName() {
		return name; 
	}
	public String getDiameter() {
		return diameter;
	
	}
	public String toString() {
		
		return name;
	}
	
}
