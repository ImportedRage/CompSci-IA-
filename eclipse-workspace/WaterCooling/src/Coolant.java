
public class Coolant {
	String name; 
	String edible;
	public Coolant(String name, String edible) {
		this.name = name; 
		this.edible = edible; 
	}
	public String getName() {
		return name; 
	}
	public String getEdible() {
		return edible;
	}
	public String toString() {
		
		return name;
	}

}
