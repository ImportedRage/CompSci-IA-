
public class Reservoir {
	String name;
	String combo; 
	public Reservoir(String name, String combo) {
		this.name = name; 	
		this.combo = combo; 

		}
	public String getName() {
		return name;
	}

	public String getCombo() {
		return combo;
	}
	public String toString() {
		
		return name;
	}
}
